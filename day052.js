// Q. Four pillar of oops 


// 1️⃣ Abstraction

// Definition (Interview):
// Abstraction means exposing only essential features and hiding internal implementation details.

// Key line to say:

// It focuses on what an object does, not how it does it.

// 2️⃣ Encapsulation

// Definition (Interview):
// Encapsulation means binding data and methods together and restricting direct access to data.

// Key line to say:

// It protects data by allowing access only through controlled methods.

// 3️⃣ Inheritance

// Definition (Interview):
// Inheritance allows a class to acquire properties and methods of another class, promoting code reusability.

// Key line to say:

// It helps reduce code duplication using parent–child relationships.

// 4️⃣ Polymorphism

// Definition (Interview):
// Polymorphism means same method name with different implementations, depending on the object or class.

// Key line to say:

// It enables flexible and dynamic behavior in OOP.